const LANGUAGE_TAGS: Record<AppLocale, string> = {
  en: 'en-US',
  fr: 'fr-FR',
  zh: 'zh-CN',
  es: 'es-ES',
  pt: 'pt-BR',
}

export function resolveLocale(language: string | null | undefined): AppLocale {
  const primaryLanguage = language
    ?.trim()
    .toLowerCase()
    .replace('_', '-')
    .split('-')[0]

  return SUPPORTED_LOCALES.includes(primaryLanguage as AppLocale)
    ? (primaryLanguage as AppLocale)
    : 'en'
}

function browserLanguage(): string | null {
  try {
    if (typeof chrome !== 'undefined' && chrome.i18n?.getUILanguage) {
      return chrome.i18n.getUILanguage()
    }
  } catch {
    // Fall through to navigator when the extension API is unavailable.
  }

  return typeof navigator !== 'undefined' ? navigator.language : null
}

export const activeLocale = resolveLocale(browserLanguage())
export const activeLanguageTag = LANGUAGE_TAGS[activeLocale]

export function translateForLocale(
  locale: AppLocale,
  key: TranslationKey,
  variables: Readonly<Record<string, string | number>> = {},
): string {
  return MESSAGES[locale][key].replace(
    /\{([a-zA-Z]+)\}/g,
    (placeholder, variable: string) =>
      variables[variable] === undefined
        ? placeholder
        : String(variables[variable]),
  )
}

export function translate(
  key: TranslationKey,
  variables?: Readonly<Record<string, string | number>>,
): string {
  return translateForLocale(activeLocale, key, variables)
}
